/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */

  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

    if(__HAL_UART_GET_FLAG(&huart1,UART_FLAG_IDLE)!=RESET) {
        __HAL_UART_CLEAR_FLAG(&huart1, UART_FLAG_IDLE);

        HAL_UART_DMAStop(&huart1);

        Rx_Length = 100 - __HAL_DMA_GET_COUNTER(&hdma_usart1_rx);
        // printf("hello world!");
        Rx_buff[Rx_Length] = '\0';
        // HAL_UART_Transmit_DMA(&huart1,Rx_buff,Rx_Length);

        printf("Receive:%s\n\r", Rx_buff);
        printf("Length:%d\n\r", Rx_Length);

        if (Rx_Length >= 16) {
            // Extracting components from Rx_buff
            char cmd[4];
            char waveform_code;
            char frequency_str[8];
            char amplitude_str[5];
            char phase_str[4];

            // Parsing the received buffer
            strncpy(cmd, Rx_buff, 3);
            cmd[3] = '\0';

            waveform_code = Rx_buff[3];

            strncpy(frequency_str, Rx_buff + 4, 7);
            frequency_str[7] = '\0';

            strncpy(amplitude_str, Rx_buff + 11, 4);
            amplitude_str[4] = '\0';

            strncpy(phase_str, Rx_buff + 15, 3);
            phase_str[3] = '\0';

            // Converting parsed strings to numerical values
            double frequency = atof(frequency_str); // Convert to double
            unsigned int amplitude = atoi(amplitude_str); // Convert to unsigned int
            unsigned int phase = atoi(phase_str); // Convert to unsigned int

            // Determine waveform mode
            unsigned int waveform_mode = SIN_WAVE;
            if (waveform_code == '0') {
                waveform_mode = SIN_WAVE;
            } else if (waveform_code == '1') {
                waveform_mode = TRI_WAVE;
            } else if (waveform_code == '2') {
                waveform_mode = SQU_WAVE;
            }

            // Set the waveform parameters on the AD9833
            AD9833_WaveSeting(frequency, 0, waveform_mode, phase); // Assuming frequence_SFR is 0
            AD9833_AmpSet((unsigned char)amplitude);
            OLED_ShowWaveParam(waveform_mode,amplitude,frequency,phase);
        }


        HAL_UART_Receive_DMA(&huart1, (uint8_t *) Rx_buff, 100);
    }

  /* USER CODE END USART1_IRQn 1 */
}

